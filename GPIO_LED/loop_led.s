                AREA |.text|, CODE, READONLY
;Reset Handler
Reset_Handler   PROC
                GLOBAL Reset_Handler
                ENTRY

AGAIN		   	LDR 	R1, =0x50000000				;Write to LED with value 0x55
				    LDR		R0, =0xFF


Loop				STR		R0, [R1]					
				    LDR		R2, =0x20				;Delay


Delay			SUBS	R2,R2,#1
    			BNE 	Delay
    			SUBS	R0, R0, #1
    			BPL     Loop
    			B       AGAIN

			
  				ALIGN 		4						; Align to a word boundary

